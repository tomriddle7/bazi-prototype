    constructor() {
        this.yeonji = "";
        this.year = null; // [수정] 결과값이 이제 string이 아닌 object 또는 null이 됩니다.
        this.month = null;
        this.day = null;
        this.hour = null;
    }

    /**
     * 사주 객체를 받아 각 자리의 신살을 계산합니다.
     * @param {Object} saju - hourKnown 속성을 가진 객체
     */
    withSaju(saju) {
        this.yeonji = saju.year.ch[1];

        this.year = this.calculate(saju.year.ch[1]);
        this.month = this.calculate(saju.month.ch[1]);
        this.day = this.calculate(saju.day.ch[1]);

        // [수정] 시주 정보가 있을 때만 12신살을 계산합니다.
        if (saju.hourKnown) {
            this.hour = this.calculate(saju.hour.ch[1]);
        } else {
            this.hour = null;
        }

        return this;
    }

    /**
     * 특정 지지의 신살을 계산하는 핵심 엔진
     * @param {string} jiji - 계산할 지지 (예: '子', '丑' 등)
     * [수정] 핵심 엔진이 이제 string 대신 object 또는 null을 반환합니다.
     */
    calculate(jiji) {
        const sinsalGroupMap = Sinsal12.SINSAL_MAP[this.yeonji];

        if (!sinsalGroupMap) {
            return null;
        }

        // PHP의 array_search(value, array)와 동일한 기능: 가로(value)로 세로(key) 찾기
        const sinsalName = Object.keys(sinsalGroupMap).find(key => sinsalGroupMap[key] === jiji);

        // 일치하는 신살이 있을 경우, DEFINITIONS를 참조하여 객체를 생성합니다.
        if (sinsalName && Sinsal12.SINSAL_DEFINITIONS[sinsalName]) {
            return {
                ko: sinsalName,
                ch: Sinsal12.SINSAL_DEFINITIONS[sinsalName].ch
            };
        }

        return null;
    }
