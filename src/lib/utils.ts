import { clsx, type ClassValue } from "clsx"
import { twMerge } from "tailwind-merge"
import { DateTime } from "luxon"

export function cn(...inputs: ClassValue[]) {
  return twMerge(clsx(inputs))
}

export const BIRTH_DATE_RGX = /([0-9]{4})([0-9]{2})([0-9]{2})/g;
export const BIRTH_TIME_RGX = /([0-9]{2})([0-9]{2})/g;

export const getYinYang = (info: { calendar: string; }) => {
  return info.calendar === 'solar' ? '양' : '음';
};

export const getBirthDate = (info: { birthDate: string; birthTime: string; gender: string; }) => {
  const birthDate = info.birthDate?.replace(BIRTH_DATE_RGX, "$1-$2-$3");
  const birthTime = info.birthTime?.replace(BIRTH_TIME_RGX, "$1:$2");
  return `${birthDate} ${birthTime} ${info.gender === 'male' ? '건명' : '곤명'}`
};

export const getDayGanji = (year: number, month: number, day: number) => {
  const gYear = month < 3 ? year - 1 : year;
  const gMonth = month < 3 ? month + 12 : month;
  const gDay = day;
  const cYear = parseInt(String(gYear / 100));
  const nYear = gYear % 100;
  const stem = 4 * cYear + Math.floor(cYear / 4) + 5 * nYear + Math.floor(nYear / 4) + Math.floor((3 * gMonth + 3) / 5) + gDay + 7;
  const gStem = SEXAGENARY.stems[STEMS[(stem - 1) % 10]];
  const branch = 8 * cYear + Math.floor(cYear / 4) + 5 * nYear + Math.floor(nYear / 4) + 6 * gMonth + Math.floor((3 * gMonth + 3) / 5) + gDay + 1;
  const gBranch = SEXAGENARY.branches[BRANCHES[(branch - 1) % 12]];
  return gStem.korean + gBranch.korean;
};

export const unsubmittAble = (personInfo: { name: string; gender: string; birthDate: any; birthTime: any; calendar: string; longitude: number; }) => {
  if (!personInfo) return true;
  const {
    name,
    gender,
    birthDate,
    birthTime,
    calendar,
    longitude,
  } = personInfo;

  if (!name) return true;
  if (gender !== 'male' && gender !== 'female') return true;
  if (birthDate?.length !== 8 || birthTime?.length !== 4) return true;
  if (!DateTime.fromISO(`${birthDate?.replace(BIRTH_DATE_RGX, "$1-$2-$3")}T${birthTime?.replace(BIRTH_TIME_RGX, "$1:$2")}`).isValid) return true;
  if (calendar !== 'solar' && calendar !== 'lunar') return true;
  if (!longitude) return true;
  return false;
};

